package com.ejemplo.tiendaalamano.models.PuntoDeVenta.Rest;


public class PuntoDeVentaRest {
    
    private String puntoDeVentaId;
    private String nombreId;
    private String correo;
    private String userName;
 
    public String getPuntoDeVentaId() {
        return puntoDeVentaId;
    }
    public void setPuntoDeVentaId(String puntoDeVentaId) {
        this.puntoDeVentaId = puntoDeVentaId;
    }
    public String getNombreId() {
        return nombreId;
    }
    public void setNombreId(String nombreId) {
        this.nombreId = nombreId;
    }
    public String getCorreo() {
        return correo;
    }
    public void setCorreo(String correo) {
        this.correo = correo;
    }
    public String getUserName() {
        return userName;
    }
    public void setUserName(String userName) {
        this.userName = userName;
    }

}
