package com.ejemplo.tiendaalamano.models.Domiciliario.Rest;


public class DomiciliarioRest {
    
    private String domiciliarioId;
    private String nombreId;
    private String correo;
    private String userName;
 
    public String getDomiciliarioId() {
        return domiciliarioId;
    }
    public void setDomiciliarioId(String domiciliarioId) {
        this.domiciliarioId = domiciliarioId;
    }
    public String getNombreId() {
        return nombreId;
    }
    public void setNombreId(String nombreId) {
        this.nombreId = nombreId;
    }
    public String getCorreo() {
        return correo;
    }
    public void setCorreo(String correo) {
        this.correo = correo;
    }
    public String getUserName() {
        return userName;
    }
    public void setUserName(String userName) {
        this.userName = userName;
    }

}
